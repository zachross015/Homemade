from enum import Enum

class Unit(Enum):
    NONE = 0    # No Units
    TSP = 1     # Teaspoon
    TBSP = 2    # Tablespoon
    FLOZ = 3    # Fluid Ounce
    GILL = 4    # About 1/2 Cup
    CUP = 5     # Cup
    PINT = 6    # Pint
    QUART = 7   # Quart
    GAL = 8     # Gallon
    ML = 9      # Milliliter
    L = 10      # Liter
    DL = 11     # Deciliter
    IB = 12     # Pound
    OZ = 13     # Ounce
    MG = 14     # Milligram
    G = 15      # Gram
    KG = 16     # Kilogram
    MM = 17     # Millimeter
    CM = 18     # Centimeter
    M = 19      # Meter
    IN = 20     # Inch
